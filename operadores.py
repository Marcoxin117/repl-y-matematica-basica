# operadores comparativos 
#  BOOLENANO presenta valores de logica binaria (falso o verdadero)
print (4 < 8)
print (4 > 8)
print (4 == 8) # == compara si es el mismo valor 4 no es igual a 8
print (4 <= 8) # mayor o igual a 
print (4 >= 8)
print (4 != 8) # ! significa diferenete o igual a 8 

print ('hola' > 'mundo') # cuenta el orden alfabetico de las palabras
print (len ('hola') > len ('bolas')) # len cuenta la cantidad de caracteres que hay en cada palabra

